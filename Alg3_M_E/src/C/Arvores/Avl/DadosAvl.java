package C.Arvores.Avl;

public class DadosAvl {
	private int num, altDir, altEsq;
	
	
	public void setAltDir(int altDir) {
		this.altDir = altDir;
	}
	
	
	public int getAltDir() {
		return altDir;
	}
	
	
	public void setAltEsq(int altEsq) {
		this.altEsq = altEsq;
	}
	
	
	public int getAltEsq() {
		return altEsq;
	}
	
	
	public void setNum(int num) {
		this.num = num;
	}
	
	
	public int getNum() {
		return num;
	}
}
