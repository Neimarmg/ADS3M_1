package M;

public class veiculos {
	static String nome;
	static float potencia;
	static int capacidadeTanque;
	
	public static String getNome() {
		return nome;
	}

	
	public static void setNome(String nome) {
		veiculos.nome = nome;
	}
	
	
	public static float getPotencia() {
		return potencia;
	}
	
	
	public static void setPotencia(float potencia) {
		veiculos.potencia = potencia;
	}
	
	
	public static int getCapacidadeTanque() {
		return capacidadeTanque;
	}
	
	
	public static void setCapacidadeTanque(int capacidadeTanque) {
		veiculos.capacidadeTanque = capacidadeTanque;
	}	
}
